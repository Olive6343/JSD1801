package net.line.model;

public class Saler {
	private String salerNo;		    //销售编号
	private String salerName;	    //销售员名字
	private String salerPwd;		//销售员登录密码
	private String salerSex;		//销售员性别
	private String salerTel;		//销售员电话
	
	
	
	public String getSalerNo() {
		return salerNo;
	}



	public void setSalerNo(String salerNo) {
		this.salerNo = salerNo;
	}



	public String getSalerName() {
		return salerName;
	}



	public void setSalerName(String salerName) {
		this.salerName = salerName;
	}



	public String getSalerPwd() {
		return salerPwd;
	}



	public void setSalerPwd(String salerPwd) {
		this.salerPwd = salerPwd;
	}



	public String getSalerSex() {
		return salerSex;
	}



	public void setSalerSex(String salerSex) {
		this.salerSex = salerSex;
	}



	public String getSalerTel() {
		return salerTel;
	}



	public void setSalerTel(String salerTel) {
		this.salerTel = salerTel;
	}



	public Saler(){
		
	}
	
}
